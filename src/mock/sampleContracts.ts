/**
 * 10 份不同主题的合同样例（用于测试）
 * 每份样例包含：文件元信息、表单预填信息、抽取字段、合同正文段落、预埋风险
 * 选中后可一键填充到「新建审核」第二步表单，并由进度页生成对应解析结果。
 */
import type {
  ContractParagraph, ExtractedField, RiskItem, RiskLevel, RiskCategory,
} from '@/types';

/** 样例合同元信息 */
export interface SampleContract {
  id: string;
  fileName: string;
  fileSize: number;
  fileTitle: string;
  contractName: string;
  contractType: string;
  myRole: 'buyer' | 'seller';
  counterparty: string;
  department: string;
  amount: number;
  reviewFocus: string[];
  reviewNote: string;
  fields: Array<{
    fieldKey: string;
    fieldLabel: string;
    fieldValue: string;
    confidence: number;
    lowConfidence: boolean;
    sourceText: string;
  }>;
  paragraphs: ContractParagraph[];
  /** 预埋风险（riskId 后缀由运行时拼上 taskId） */
  risks: Array<Omit<RiskItem, 'id' | 'reviewTaskId' | 'handler' | 'handleComment' | 'ignoreReason' | 'version' | 'createdAt' | 'updatedAt'> & { editedSuggestion: null | string; ruleId: string | null }>;
}

/** 通用风险模板构造器 */
function risk(
  partial: {
    title: string;
    riskType: RiskCategory;
    riskLevel: RiskLevel;
    clauseNumber: string;
    clauseTitle: string;
    originalText: string;
    paragraphId: string;
    riskReason: string;
    reviewBasis: string;
    suggestion: string;
    confidence: number;
    sourceType: 'rule' | 'ai';
    status: 'pending';
    startPosition: number;
    endPosition: number;
    ruleId?: string;
  },
): SampleContract['risks'][number] {
  return { ...partial, editedSuggestion: null, ruleId: partial.ruleId ?? null };
}

